import UIKit

extension UIColor {
    @nonobjc class var red: UIColor {
        return UIColor(red: 252.0 / 255.0, green: 63.0 / 255.0, blue: 56.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var tapRed: UIColor {
        return UIColor(red: 242.0 / 255.0, green: 0.0, blue: 0.0, alpha: 1.0)
    }
}
